public class SelectiveDisclosureCredential {

    /**
     * Required: The Credential.
     */
    private CredentialPojo credential;

    /**
     * Required: The salts corresponding to the credential. The structure is same as specific cpt
     * type structure. There are three class files for one CPT, for CPT101, a example of this is
     * there are three class files generated by weidentity-build-tool, first one is Cpt101.java,
     * second one is Cpt101salt.java. And third one is Cpt101FieldsToBeDisclosed.java
     */
    private Map<String, Object> salt;
}
